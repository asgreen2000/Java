    static int pairsOfSongs(int[] arr, int time) {

        Hashtable<Integer, Integer> hashtable = new Hashtable<>();
        int count = 0;
        for (int i = 0; i < arr.length; i++)
            arr[i] = arr[i] % time;
        
        for (int num : arr) {

            int res = time - num;
            count += hashtable.getOrDefault(res % time, 0);
            hashtable.put(num, hashtable.getOrDefault(num, 0) + 1);
        }

        return count;
    }

